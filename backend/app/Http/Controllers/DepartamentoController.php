    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $nombreDepartamento = strtolower($request->nombreDepartamento);
        $geBusc = Departamento::where('nombreDepartamento','=',$nombreDepartamento)->first();
        if(isset($geBusc)){
          return response()->json([
            "message"=>"Ya existe un departamento con el mismo nombre"
          ]);
        }
        else{
          $departamento = new Departamento();
          $departamento->nombreDepartamento = $nombreDepartamento;
          $departamento->save();
          return response()->json([
            "message" => "Se ha creado un nuevo departamento"
          ]);
        }
    }

